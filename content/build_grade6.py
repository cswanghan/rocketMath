def _foi_fact(n, d, k, ans):
    """分数乘整数 fluency fact."""
    assert n * k % d == 0 and n * k // d == ans, (
        f"CHECK FAIL: {n}/{d} × {k} should be {n * k // d}, got {ans}"
    )
    fid = f"foi_{n}o{d}x{k}"
    prompt = f"{n}/{d} × {k} = ?"
    return {"id": fid, "prompt": prompt, "answer": ans, "learningType": "pattern"}


def build_foi_levels():
    levels = []
