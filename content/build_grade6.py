    kmap = build_knowledge_map(practice)
    kmap_path = os.path.join(CONTENT_DIR, "grade6_knowledge_map.json")
    with open(kmap_path, "w", encoding="utf-8") as f:
        json.dump(kmap, f, ensure_ascii=False, indent=2)
        f.write("\n")

