B, C, X = "basic", "consolidate", "challenge"


def col(a, b, op):
    w = max(len(str(a)), len(str(b)) + 2) + 1
    return f"{str(a).rjust(w)}\n{(op + ' ' + str(b)).rjust(w)}\n{'─' * w}"


def P_fill(pid, prompt, answer, hint, expl, diff=C):
    return {"id": pid, "type": "fill", "prompt": prompt, "difficulty": diff,
            "answer": answer, "hint": hint, "explanation": expl}


def P_mc(pid, prompt, opts, ci, hint, expl, diff=C):
    ch = [{"id": chr(97 + i), "label": str(l), "correct": i == ci} for i, l in enumerate(opts)]
    return {"id": pid, "type": "mc", "prompt": prompt, "difficulty": diff,
            "choices": ch, "hint": hint, "explanation": expl}


def P_steps(pid, prompt, fields, hint, expl, diff=C):
    return {"id": pid, "type": "steps", "prompt": prompt, "difficulty": diff,
            "fields": fields, "hint": hint, "explanation": expl}


def make_set(setid, title, pedagogy, problems, max_tries=2):
    return {"id": setid, "title": title, "pedagogy": pedagogy, "maxTries": max_tries, "problems": problems}


def _apply(op, a, b):
    return {"+": a + b, "−": a - b, "×": a * b, "÷": a // b}[op]


def col_set(setid, title, pedagogy, op, hint, basic, consol, chall, word=None):
    """Build a 竖式/算式 set across tiers. word: optional (prompt, answer, diff)."""
    probs = []
    for tag, diff, pairs in [("b", B, basic), ("c", C, consol), ("x", X, chall)]:
        for i, (a, b) in enumerate(pairs):
            ans = _apply(op, a, b)
            probs.append(P_fill(f"{setid}_{tag}{i}", col(a, b, op), ans, hint, f"{a} {op} {b} = {ans}", diff))
    for i, (p, a, d, ex) in enumerate(word or []):
        probs.append(P_fill(f"{setid}_w{i}", p, a, hint, ex, d))
    return make_set(setid, title, pedagogy, probs)


